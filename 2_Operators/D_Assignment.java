public class D_Assignment {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        // Assignment operators
        a += b; // a = a + b
        System.out.println("After a += b, a = " + a);

        a -= b; // a = a - b
        System.out.println("After a -= b, a = " + a);

        a *= b; // a = a * b
        System.out.println("After a *= b, a = " + a);

        a /= b; // a = a / b
        System.out.println("After a /= b, a = " + a);

        a %= b; // a = a % b
        System.out.println("After a %= b, a = " + a);
    }
}
